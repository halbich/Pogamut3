package SteeringStuff;

/**
 * The reference boolean value.
 * @author Marki
 */
public class RefBoolean {

    private boolean value;

    public RefBoolean(boolean value) {
        this.value = value;
    }

    public boolean getValue() {
        return value;
    }

    public void setValue(boolean value) {
        this.value = value;
    }
}
